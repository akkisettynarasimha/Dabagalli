package com.arkainfoteck.dabagalli.models;

public class FoodTypeModel {

    String id;
    String name;

    public FoodTypeModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
